pub fn build_titles (keywords: &Vec<String>, num_titles: u32) -> Vec<String> {
    let mut rng = thread_rng();

    let mut out_titles = Vec::new();

    for _ in 0..num_titles {
        // take prefix and suffix from randomly chosen title
        let (prefix, suffix) = TITLE_PARTS[rng.gen_range::<usize>(
            // need to cast u32 as usize so that
            // SliceIndex constraints are satisfied
            0, TITLE_NUM as usize
        )];

        let title = format!(
            "{}{}{}",

            prefix,

            // from keywords..
            keywords[
                // get a random keyword
                rng.gen_range::<usize>(
                    // where index is between 0 and
                    // total number of keywords
                    0, keywords.len() as usize
                )
            ].to_capitalized(),

            suffix
        );

        out_titles.push(title);
    }

    out_titles
}